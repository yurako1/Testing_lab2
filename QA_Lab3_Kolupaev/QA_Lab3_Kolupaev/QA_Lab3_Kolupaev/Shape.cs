using System;

namespace RectangleApp
{
    /**
    * @brief Абстрактный базовый класс для всех геометрических фигур
    * @details Определяет общий интерфейс для работы с фигурами.
    * Реализует паттерн "Шаблонный метод" через абстрактные методы.
    * 
    * Формулы для основных операций:
    * - Площадь прямоугольника: S = (x₂ - x₁) × (y₁ - y₂)
    * 
    * Тестовые данные:
    * (координаты: (2,5) и (10,1), площадь = 32)
    */
    public abstract class Shape
    {
        /**
        * @brief Отображает информацию о фигуре
        * @details Реализация должна включать все основные характеристики фигуры
        */
        public abstract void Display();

        /**
        * @brief Вычисляет площадь фигуры
        * @return Площадь фигуры в виде числа с плавающей точкой
        * @exception ArithmeticException Может возникать при переполнении при вычислениях
        */
        public abstract double CalculateArea();

        /**
        * @brief Выводит информацию о площади фигуры в консоль
        * @example Пример вывода: "Площадь: 25.5"
        */
        public void PrintArea()
        {
            Console.WriteLine($"Площадь: {CalculateArea()}");
        }
    }
}